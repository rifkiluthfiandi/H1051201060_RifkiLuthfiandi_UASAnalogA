    private boolean cekJikaKosong(boolean hewan) {
        boolean hasil = false;

        if (hewan == true) {
            if (jTextField_JumlahHewan.getText().isEmpty() == true) {
                JOptionPane.showMessageDialog(null, "Silakan isi jumlah item 'Hewan' yang akan dipesan.");
                hasil = true;
            }
        } else {
            if (jTextField_JumlahPerlengkapan.getText().isEmpty() == true) {
                JOptionPane.showMessageDialog(null, "Silakan isi jumlah item 'Perlengkapan Lainnya' yang akan dipesan.");
                hasil = true;
            }
        }
        return hasil;
    }

