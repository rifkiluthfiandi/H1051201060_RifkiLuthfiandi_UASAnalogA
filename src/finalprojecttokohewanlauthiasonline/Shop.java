    private void setTabelKeranjang() {

        int hargatotalbarang = 0;
        int ongkoskirim = 0;
        int totalpembayaran = 0;

        DefaultTableModel tab = new DefaultTableModel();
        tab.addColumn("Nama Unit");
        tab.addColumn("Jumlah");
        tab.addColumn("Harga");
        tab.addColumn("Sub Total Harga");

        for (int n = 0; n < namaunit.size(); n++) {
            hargatotalbarang = hargatotalbarang + Integer.parseInt(subtotalharga.get(n).toString());

            tab.addRow(new Object[]{
                namaunit.get(n),
                jumlah.get(n),
                harga.get(n),
                subtotalharga.get(n)
            });
        }

        jTable_TabelKeranjang.setModel(tab);

        ongkoskirim = 30000;
        totalpembayaran = (int) (hargatotalbarang + ongkoskirim);

        jTextField_HargaTotalBarang.setText("Rp " + hargatotalbarang);
        jTextField_OngkosKirim.setText("Rp 30000");
        jTextField_TotalPembayaran.setText("Rp " + totalpembayaran);

    }
